package org.cloudfoundry.identity.uaa.test;

import org.cloudfoundry.identity.uaa.config.YamlServletProfileInitializerForTests;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.ApplicationContext;
import org.springframework.security.web.FilterChainProxy;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;
import org.springframework.test.context.web.WebAppConfiguration;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.web.context.WebApplicationContext;

@RunWith(SpringJUnit4ClassRunner.class)
@WebAppConfiguration
@ContextConfiguration(locations = "file:./src/main/webapp/WEB-INF/spring-servlet.xml", initializers = YamlServletProfileInitializerForTests.class)
abstract public class AppContextTest {
    @Autowired protected ApplicationContext webApplicationContext;
    protected MockMvc mockMvc;

    public void setUp() throws Exception {
        FilterChainProxy springSecurityFilterChain = webApplicationContext.getBean("springSecurityFilterChain", FilterChainProxy.class);

        mockMvc = MockMvcBuilders.webAppContextSetup((WebApplicationContext) webApplicationContext).addFilter(springSecurityFilterChain)
            .build();
    }
}
